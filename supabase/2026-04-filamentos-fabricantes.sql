CREATE TABLE IF NOT EXISTS public.cadastro_fabricantes_filamentos (
    id_fabricante_filamento BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_fabricante TEXT NOT NULL UNIQUE,
    site_fabricante TEXT NULL,
    observacoes TEXT NULL,
    ativo BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

